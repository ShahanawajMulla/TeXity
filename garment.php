	<p>		        Sequence of Garments Manufacturing are given below in</p>
<p>details:</p>
<p>1. Design/ Sketch:</p>
<p>
In the garment manufacturing the first step is designing the sketch for the dresses that have to be
prepared. For this purpose the designer first draw several rough sketches in the sketch book. The
designer does not go for details at this moment but he rather let his creativity flow on the paper
and he draws many sketches. Later these sketches are analyzed by a panel of designers. They
finally select few out of them. These few sketches are rendered in detail separately or in the form
of a single collection. The designer also draws working drawings along with the sketch. Working
drawings are flat drawing of the sketch and it help pattern maker in understanding the patterns
involved in the construction.</p>

<p>2. Pattern Design:</p><p>
Hard paper copy of each component of the garment of exact dimension of each component is
called pattern. The patterns also include seam allowance, trimming allowance, dirts, and pleats,
ease allowance, any special design etc affairs. Pattern design could also be done manually or
with the help of computer.</p>
<p>
3. Sample Making:</p>
<p>The first patterns are sent to the sewing unit for assembling them into garment. This is usually
stitched on calico or muslin which is an inferior quality of fabric and it reduces cost. This sample
is constructed to analyze the pattern fit and design too. After the sample garment is stitched it is
reviewed by a panel of designers, pattern makers and sewing specialists. If any changes have to
be made they are made at this time.
</p><p>
4. Production Pattern:</p><p>
The pattern design is now taken for creating the production patterns. The production pattern is one
which will be used for huge production of garments. The pattern maker makes the patterns on
standard pattern making paper. These papers are made-up of various grades. The most important
component, the tissue paper pattern, is made from the lightest and thinnest paper commercially
available (it is not made at the pattern companies). It is called 7.5 lb (3.4 kg) basis paper,
meaning that a ream of it (500 sheets) only weighs 7.5 lb (3.4 kg).
Garment patterns can be constructed by two means: manual method, CAD/CAM method.
Today many companies have developed CAD/CAM because of the ease of designing patterns,
fluency and precision involved which cannot be guaranteed with the manual method. Investing
once into the CAD/CAM unit is worth in itself. Many buyers around the world prefer manufacturers
who are using CAD/CAM methods. The production patterns created in CAD/CAM can be stored
easily and they can be modified at any point of time.
A garment sewing pattern or garment fabric & patterns draft is developed by calculating, taking
account of the following measurements: -</p>
<p>1. Direct Sample.</p>
<p>2. Specification Sheet/ Measurement Chart.
</p><p>3. Actual body size measurements.
</p><p>4. Ease Allowances.
</p><p>5. Sewing Allowance.
</p><p>These allowances are different for different type of fabrics and patterns.
</p><p>5. Grading:
</p><p>The purpose of grading is to create patterns in different standard sizes. Grading a pattern is really
scaling a pattern up or down in order to adjust it for multiple sizes. Pattern sizes can be large,
