</p><p>11. Inspection:
</p><p>Each and every garment after sewing passes through the inspection table/ point, where the
garments are thoroughly and carefully checked to detect/find any defect if present in the garment.
The defects may be for example variation of measurement, sewing defect, fabric defects, spots
etc. if the defect is possible to overcome, then the garment is sent to the respective person for
correction. If the defect is not correctionable, then the garment is separated as wastage.
</p><p>12. Pressing/ Finishing:
</p><p>After passing through the inspection table, each garment is normally ironed/ pressed to remove
unwanted crease and to improve the smoothness, so that the garments looks nice to the
customer. Folding of the garment is also done here for poly packing of the garments as per
required dimension.
</p><p>13. Final Inspection:
</p><p>It is the last stage of inspection f the manufactured garments on behalf of the garment
manufacturing organization, to detect any defective garments before packing.
</p><p>14. Packing:
</p><p>After final inspection, the garments are poly-packed, dozen-wise, color wise, size ratio wise,
bundled and packed in the cartoon. The cartoon is marked with important information in printed
form which is seen from outside the cartoon easily.
</p><p>15. Despatch:
</p><p>The cartoons of the manufactured garments are delivered or placed in the despatch department or
finished product godown, from where the garments lot is delivered for shipment.
