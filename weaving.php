	The principal parts of a loom are the frame, the warp-beam or weavers beam, the cloth-roll (apron
bar), the heddles, and their mounting, the reed. The warp-beam is a wooden or metal cylinder on
the back of the loom on which the warp is delivered. The threads of the warp extend in parallel
order from the warp-beam to the front of the loom where they are attached to the cloth-roll. Each
thread or group of threads of the warp passes through an opening (eye) in a heddle. The warp
threads are separated by the heddles into two or more groups, each controlled and automatically
drawn up and down by the motion of the heddles. In the case of small patterns the movement of
the heddles is controlled by “cams” which move up the heddles by means of a frame called a
harness; in larger patterns the heddles are controlled by a dobby mechanism, where the healds
are raised according to pegs inserted into a revolving drum. Where a complex design is required,
the healds are raised by harness cords attached to a Jacquard machine. Every time the harness
(the heddles) moves up or down, an opening (shed) is made between the threads of warp, through
which the pick is inserted. Traditionally the weft thread is inserted by a shuttle.
</p>
<p>
On a conventional loom, the weft thread is carried on a pirn, in a shuttle that passes through the
shed. A handloom weaver could propel the shuttle by throwing it from side to side with the aid of a
picking stick. The "picking΅ on a power loom is done by rapidly hitting the shuttle from each side
using an overpick or underpick mechanism controlled by cams 80-250 times a minute. When a
pirn is depleted, it is ejected from the shuttle and replaced with the next pirn held in a battery
attached to the loom. Multiple shuttle boxes allow more than one shuttle to be used. Each can
carry a different colour which allows banding across the loom.
The rapier-type weaving machines do not have shuttles, they propel the weft by means of small
grippers or rapiers that pick up the filling thread and carry it halfway across the loom where
another rapier picks it up and pulls it the rest of the way. Some carry the filling yarns across the
loom at rates in excess of 2,000 meters per minute. Manufacturers such as Picanol have reduced
